package Estrutura_condicional;

public class OpedradoresComparativos {
	
	
	public static void main (String[] args) throws Exception {
		int num = 5;
		int num2 = 4;
		System.out.println(num < num2);
		System.out.println(num <= num2);
		System.out.println(num > num2);
		System.out.println(num >= num2);
		System.out.println(num == num2);
		System.out.println(num != num2);
		
		
		
	}
	
	
	

}

// aulas sobre operadores aritimeticos. 

let num1 = 5;
let num2 = 10;
console.log(num1 + num2);

// -> ** <- esse simbolo e de potenciação 
console.log(num1 ** num2);

// -> % <- resto da divisao. 
console.log(num1 % num2);

let contador = 2;
contador++;// O ++ já faz a soma do valor de 1 em 1 isso e o operador de incremento   
++contador;
console.log(contador);
console.log(--contador);